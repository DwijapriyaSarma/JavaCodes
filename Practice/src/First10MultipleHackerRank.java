public class First10MultipleHackerRank {
    public static void main(String[] args) {
        int N=2;
        int result=1;


        for(int i=1;i<=10;i++){
            result=N*i;


            System.out.print(+N +" "+"*"+" "+i+" "+"=" +result + " ");
            System.out.println();

            //result*=1;
        }



    }




}
