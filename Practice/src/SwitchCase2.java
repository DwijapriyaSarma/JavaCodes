public class SwitchCase2 {
    public static void main(String[] args) {
        byte ba=127;

        switch (ba){

            case 127:
                System.out.println("Maina");
           break;
            case 'a':
                System.out.println("Bubu");
                break;

            default:
                System.out.println("Hello");

        }
    }
}
